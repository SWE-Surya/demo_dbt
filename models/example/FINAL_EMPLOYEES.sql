SELECT
    ID,
    FIRST_NAME,
	LAST_NAME,
	LOCATION,
	DEPARTMENT,
	EMAIL,
	CONCAT(FIRST_NAME, ' ', LAST_NAME) AS FULL_NAME, 
	SALARY, 
	CASE 
	WHEN SALARY>20000 
	THEN TRUE
	ELSE 
	FALSE END AS TAXABLE, 
	CASE 
	WHEN SALARY>20000 
	THEN 0.05*SALARY 
	ELSE 
	0 END AS TAX, 
FROM  {{ ref('INTMD_EMPLOYEES') }}

